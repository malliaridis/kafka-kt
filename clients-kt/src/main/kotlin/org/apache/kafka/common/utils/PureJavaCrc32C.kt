    /**
     * The current CRC value, bit-flipped.
     */
    private var crc = 0

    override fun getValue(): Long {
        val ret = crc.toLong()
        return ret.inv() and 0xffffffffL
    }

    override fun reset() {
        crc = -0x1
    }

    override fun update(bytes: ByteArray, offset: Int, length: Int) {
        var off = offset
        var len = length
        var localCrc = crc

        while (len > 7) {
            val c0 = bytes[off + 0].toInt() xor localCrc and 0xff
            val c1 = bytes[off + 1].toInt() xor 8.let {
                localCrc = localCrc ushr it; localCrc
            } and 0xff
            val c2 = bytes[off + 2].toInt() xor 8.let {
                localCrc = localCrc ushr it; localCrc
            } and 0xff
            val c3 = bytes[off + 3].toInt() xor 8.let {
                localCrc = localCrc ushr it; localCrc
            } and 0xff
            localCrc = (T[T8_7_START + c0] xor T[T8_6_START + c1]
                    xor (T[T8_5_START + c2] xor T[T8_4_START + c3]))
            val c4 = bytes[off + 4].toInt() and 0xff
            val c5 = bytes[off + 5].toInt() and 0xff
            val c6 = bytes[off + 6].toInt() and 0xff
            val c7 = bytes[off + 7].toInt() and 0xff
            localCrc = localCrc xor (T[T8_3_START + c4] xor T[T8_2_START + c5]
                    xor (T[T8_1_START + c6] xor T[T8_0_START + c7]))
            off += 8
            len -= 8
        }
        when (len) {
            7 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            6 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            5 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            4 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            3 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            2 -> {
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
                localCrc =
                    localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]
            }

            1 -> localCrc =
                localCrc ushr 8 xor T[T8_0_START + (localCrc xor bytes[off++].toInt() and 0xff)]

            else -> {}
        }

        // Publish crc out to object
        crc = localCrc
    }

    override fun update(b: Int) {
        crc = crc ushr 8 xor T[T8_0_START + (crc xor b and 0xff)]
    }

